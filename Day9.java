package Day9;

public class Main {
    
    public static void main(String[] args) {
        byte jumlah; //Deklarasi
        jumlah = 2; //Inisialisasi
        jumlah = 3; // Update variabel
        System.out.println(jumlah);
        
        /* Deklarasi itu adalah membuat tipe data dan variabelnya.
         * Inisialisasi itu adalah memberikan nilai pada variabel 
         * yang sudah dideklarasikan.
         * Update variabel itu adalah mengubah nilai variabel yg sudah
         * dibuat sebelumnya.
         */
    }
    
}
